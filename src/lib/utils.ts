import { type ClassValue, clsx } from "clsx"
import { twMerge } from "tailwind-merge"
 
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs))
}

// Function to generate accessible alt text for images
export function getImageAltText(title: string, author: string): string {
  return `${title} - Blog article by ${author}`
}

